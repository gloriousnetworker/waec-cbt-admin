/**
 * scripts/generate-firebase-sw.js
 * Reads Firebase config from .env.local and writes
 * public/firebase-messaging-sw.js so the file never contains
 * hardcoded credentials and is never committed to source control.
 *
 * Run automatically via `predev` and `prebuild` npm hooks.
 */
const fs   = require('fs');
const path = require('path');

// ── minimal .env parser (avoids external dependency) ─────────────────────────
function loadEnv(filePath) {
  if (!fs.existsSync(filePath)) return {};
  return fs.readFileSync(filePath, 'utf8')
    .split('\n')
    .reduce((acc, line) => {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) return acc;
      const eqIdx = trimmed.indexOf('=');
      if (eqIdx === -1) return acc;
      const key = trimmed.slice(0, eqIdx).trim();
      const val = trimmed.slice(eqIdx + 1).trim().replace(/^['"]|['"]$/g, '');
      acc[key] = val;
      return acc;
    }, {});
}

const root = path.resolve(__dirname, '..');
// Merge: .env < .env.local (local takes precedence, same as Next.js)
const env = { ...loadEnv(path.join(root, '.env')), ...loadEnv(path.join(root, '.env.local')) };

const required = [
  'NEXT_PUBLIC_FIREBASE_API_KEY',
  'NEXT_PUBLIC_FIREBASE_PROJECT_ID',
  'NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID',
  'NEXT_PUBLIC_FIREBASE_APP_ID',
];

const missing = required.filter(k => !env[k]);
if (missing.length) {
  console.error('❌  generate-firebase-sw: missing env vars:', missing.join(', '));
  console.error('    Create a .env.local file with those values.');
  process.exit(1);
}

const sw = `// AUTO-GENERATED by scripts/generate-firebase-sw.js — do not edit manually.
// Re-run \`npm run generate-sw\` (or \`npm run dev\` / \`npm run build\`) to regenerate.
importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-messaging-compat.js');

firebase.initializeApp({
  apiKey:            '${env.NEXT_PUBLIC_FIREBASE_API_KEY}',
  projectId:         '${env.NEXT_PUBLIC_FIREBASE_PROJECT_ID}',
  messagingSenderId: '${env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID}',
  appId:             '${env.NEXT_PUBLIC_FIREBASE_APP_ID}',
});

const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const { title, body } = payload.notification || {};
  if (!title) return;
  self.registration.showNotification(title, {
    body: body || '',
    icon: '/logo.png',
    badge: '/logo.png',
    tag: payload.data?.type || 'notification',
    data: payload.data || {},
    requireInteraction: false,
  });
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then((clientList) => {
      for (const client of clientList) {
        if (client.url.includes('/dashboard') && 'focus' in client) return client.focus();
      }
      if (clients.openWindow) return clients.openWindow('/dashboard');
    })
  );
});
`;

const outPath = path.join(root, 'public', 'firebase-messaging-sw.js');
fs.writeFileSync(outPath, sw);
console.log('✅  firebase-messaging-sw.js generated from .env.local');
